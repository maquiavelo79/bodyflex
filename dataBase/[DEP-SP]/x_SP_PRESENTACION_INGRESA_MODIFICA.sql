CREATE PROCEDURE bodyflex.`SP_PRESENTACION_INGRESA_MODIFICA`( 
                                                IN rut VARCHAR(20)  ,
                                                IN flic VARCHAR(28) ,
                                                IN tex VARCHAR(2000)
                                              )
BEGIN
DECLARE EXIT HANDLER FOR SQLEXCEPTION SELECT '99';

    -- 1 ACTUALIZAR EL REGISTRO
      UPDATE PROFESIONAL 
      SET PFOTOPRE=flic
      , PTXTOPRE=tex
      WHERE PRUT=rut;
      SELECT 1;
        
END;
