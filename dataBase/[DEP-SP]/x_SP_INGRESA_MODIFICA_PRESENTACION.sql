DROP PROCEDURE IF EXISTS bodyflex.SP_INGRESA_MODIFICA_PRESENTACION;
CREATE PROCEDURE bodyflex.`SP_INGRESA_MODIFICA_PRESENTACION`( 
                                                IN rut VARCHAR(20)  ,
                                                IN flic VARCHAR(20) ,
                                                IN tex VARCHAR(2000)
                                              )
BEGIN

    -- 1 ACTUALIZAR EL REGISTRO
      UPDATE PROFESIONAL 
      SET PIDFOTOFLICKR=flic
      , PTXTOPRE=tex
      WHERE PRUT=rut;
      SELECT 1;
        
END;
