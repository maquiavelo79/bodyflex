DROP PROCEDURE IF EXISTS bodyflex.SP_CONSULTA_REFERENCIAS_ARTICULO;
CREATE PROCEDURE bodyflex.`SP_CONSULTA_REFERENCIAS_ARTICULO`(
                                                              IN id VARCHAR(50)
                                                          )
BEGIN 
DECLARE EXIT HANDLER FOR SQLEXCEPTION SELECT '99'; 
  
  SELECT PR.PRID AS 'ID'
  , PR.PRNOM AS 'NOMBRE'
  , (SELECT R.RETIPO FROM PUBLICACION_LIST_REFERENCIA R WHERE R.REID = PR.REID) AS 'TIPO'
  , PR.PRDES AS 'DESCRIPCION'
  FROM PUBLICACION_REFERENCIA PR
  WHERE PR.PUID=id
  ORDER BY PR.PRID ASC;
  
END;




